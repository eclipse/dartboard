package org.eclipse.dartboard;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class DartBoardPlugin extends AbstractUIPlugin {
	public static final String PLUGIN_ID = "org.eclipse.dartboard"; //$NON-NLS-1$
	private static DartBoardPlugin plugin;

	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	public static DartBoardPlugin getDefault() {
		return plugin;
	}
}
